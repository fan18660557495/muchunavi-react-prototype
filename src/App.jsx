const ZH_TO_JA = Object.fromEntries(
  Object.entries(JA_TO_ZH)
    .slice()
    .reverse()
    .map(([ja, zh]) => [zh, ja]),
);

function translateText(value, locale) {
  const dictionary = locale === 'zh' ? JA_TO_ZH : ZH_TO_JA;
  const entries = Object.entries(dictionary).sort((a, b) => b[0].length - a[0].length);
  let nextValue = value;

  for (const [from, to] of entries) {
    if (nextValue.includes(from)) {
      nextValue = nextValue.split(from).join(to);
    }
  }

  return nextValue;
}

function getInitialLocale() {
  const stored = window.localStorage.getItem(MU_LOCALE_STORAGE_KEY);
  return stored === 'zh' || stored === 'ja' ? stored : 'ja';
}

