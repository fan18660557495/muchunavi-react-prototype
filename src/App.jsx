function App() {
  const [activeId, setActiveId] = useState('home');
  const [stack, setStack] = useState([]);
  const [locale, setLocale] = useState(getInitialLocale);
  const screenRef = useRef(null);

  const screenMap = useMemo(() => new Map([...mainTabs, ...secondaryScreens].map((page) => [page.id, page])), []);

  const currentScreenId = stack.length > 0 ? stack[stack.length - 1] : activeId;
  const activePage = useMemo(() => screenMap.get(currentScreenId) ?? mainTabs[0], [currentScreenId, screenMap]);

  useEffect(() => {
    window.__nav = {
      open: (id) => {
        if (!screenMap.has(id)) return;
        setStack((prev) => [...prev, id]);
      },
      back: () => {
        setStack((prev) => prev.slice(0, -1));
      },
      switchTab: (id) => {
        setActiveId(id);
        setStack([]);
      },
    };

    return () => {
      delete window.__nav;
    };
  }, [screenMap]);

  const ActiveScreen = activePage.component;
  const isRootTab = stack.length === 0;

  useEffect(() => {
    document.documentElement.lang = locale === 'zh' ? 'zh-CN' : 'ja';
    window.localStorage.setItem(MU_LOCALE_STORAGE_KEY, locale);
    window.__MU_LOCALE = locale;
    window.dispatchEvent(new CustomEvent('mu-locale-change', { detail: { locale } }));

    const root = screenRef.current;
    if (!root) {
      return undefined;
    }

    let isApplying = false;
    let rafId = null;
    let observer = null;

    const startObserving = () => {
      observer?.observe(root, {
        childList: true,
        subtree: true,
        characterData: true,
      });
    };

    const applyTranslation = () => {
      observer?.disconnect();
      isApplying = true;

      const walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT, {
        acceptNode(node) {
          if (!node?.nodeValue?.trim()) {
            return NodeFilter.FILTER_REJECT;
          }
          const parentTag = node.parentElement?.tagName;
          if (parentTag === 'SCRIPT' || parentTag === 'STYLE' || parentTag === 'TEXTAREA') {
            return NodeFilter.FILTER_REJECT;
          }
          if (node.parentElement?.closest('input, textarea, [contenteditable="true"], [data-no-global-translate="true"]')) {
            return NodeFilter.FILTER_REJECT;
          }
          return NodeFilter.FILTER_ACCEPT;
        },
      });

      const textNodes = [];
      while (walker.nextNode()) {
        textNodes.push(walker.currentNode);
      }

      textNodes.forEach((node) => {
        const translated = translateText(node.nodeValue, locale);
        if (translated !== node.nodeValue) {
          node.nodeValue = translated;
        }
      });

      isApplying = false;
      rafId = null;
      startObserving();
    };

    applyTranslation();

    observer = new MutationObserver(() => {
      if (!isApplying) {
        if (rafId !== null) {
          window.cancelAnimationFrame(rafId);
        }
        rafId = window.requestAnimationFrame(applyTranslation);
      }
    });
    startObserving();

    return () => {
      observer?.disconnect();
      if (rafId !== null) {
        window.cancelAnimationFrame(rafId);
      }
    };
  }, [currentScreenId, locale]);

  return (
    <main className="min-h-screen bg-[#f6f2ef] text-[#1a2c31]">
      <div className="mx-auto min-h-screen max-w-[440px] bg-[#f6f2ef] shadow-[0_0_0_1px_rgba(164,150,115,0.06)]">
        <div ref={screenRef} className="h-[100dvh] overflow-hidden bg-[#f6f2ef]">{ActiveScreen ? <ActiveScreen /> : null}</div>
      </div>
      {isRootTab ? (
        <TabBar
          activeId={activeId}
          onChange={(id) => {
            setActiveId(id);
            setStack([]);
          }}
        />
      ) : null}
      <FloatingTranslator locale={locale} isRootTab={isRootTab} onToggle={() => setLocale((current) => (current === 'ja' ? 'zh' : 'ja'))} />
    </main>
  );
}

export default App;
